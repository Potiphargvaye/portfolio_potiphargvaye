document.getElementById("projectRequestForm")
.addEventListener("submit", function(e){

    e.preventDefault();

    const formData = new FormData(this);

    fetch(this.action, {
        method: "POST",
        body: formData
    })
    .then(res => res.text())
    .then(data => {

        if(data.trim() === "success"){
            showNotification("success", "Request submitted successfully!");
            this.reset();
        }
        else if(data.trim() === "empty"){
            showNotification("error", "Please enter your request.");
        }
        else{
            showNotification("error", "Something went wrong.");
        }

    })
    .catch(() => {
        showNotification("error", "Server connection error.");
    });

});

